n = gets.to_i

count = 0
case n.to_s.split("").length
when 4, 5, 6
  count = n - 1000 + 1
when 7,8,9
  count = (n - 1000000 + 1) * 2 + 1000000 - 1000
when 10,11,12
  count = (n - 1000000000 + 1) * 3 + (1000000000 - 1000000) * 2 + 1000000 - 1000
when 13,14,15
  count = (n - 1000000000000 + 1) * 4 + (1000000000000 - 1000000000) * 3 + (1000000000 - 1000000) * 2 + 1000000 - 1000
when 16
  count = 5 + (1000000000000000 - 1000000000000) * 4 + (1000000000000 - 1000000000) * 3 + (1000000000 - 1000000) * 2 + 1000000 - 1000
end

puts count